#!/usr/bin/env python3
"""
Run FinBalance evaluation against one or more models.

Backend is selected automatically:
  - Model IDs starting with "claude-"  -> Anthropic API  (ANTHROPIC_API_KEY)
  - Everything else                    -> OpenRouter API  (OPENROUTER_API_KEY)

Usage:
  # Quick smoke test with Anthropic
  python scripts/run_evaluation.py --model claude-sonnet-4-6 --quick

  # Quick smoke test with OpenRouter
  python scripts/run_evaluation.py --model openai/gpt-4.1-mini --quick

  # Full dataset, multiple models and strategies
  python scripts/run_evaluation.py \\
    --dataset data/dataset.jsonl \\
    --models claude-sonnet-4-6,openai/gpt-4.1-mini \\
    --strategies zero_shot,cot \\
    --output results/
"""

import argparse
import json
import os
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

from finbalance.analysis.error_detection import aggregate_errors
from finbalance.data.generation.generator import ProblemGenerator
from finbalance.data.schemas import Problem
from finbalance.evaluation.metrics.core import aggregate
from finbalance.evaluation.models.anthropic_model import AnthropicModel
from finbalance.evaluation.models.base import ModelConfig
from finbalance.evaluation.models.openrouter import OpenRouterModel
from finbalance.evaluation.runner import EvaluationRunner


def build_model(model_id: str, api_key: str | None, seed: int) -> AnthropicModel | OpenRouterModel:
    """Auto-select backend: Anthropic for claude-* models, OpenRouter for everything else."""
    config = ModelConfig(model_id=model_id, temperature=0.0, seed=seed)
    if model_id.startswith("claude-"):
        return AnthropicModel(config, api_key=api_key or os.environ.get("ANTHROPIC_API_KEY"))
    return OpenRouterModel(config, api_key=api_key or os.environ.get("OPENROUTER_API_KEY"))


def load_dataset(path: str) -> list[Problem]:
    """Load problems from a JSONL file generated by generate_dataset.py."""
    import dataclasses

    problems = []
    with open(path) as f:
        for line in f:
            d = json.loads(line)
            # Reconstruct Problem from dict (simplified — uses dict directly)
            problems.append(_dict_to_problem(d))
    return problems


def _dict_to_problem(d: dict) -> Problem:
    """Reconstruct a Problem dataclass from its dict serialisation."""
    from finbalance.data.schemas import (
        Adjustment,
        BalanceSheet,
        IntermediateState,
        JournalEntry,
        OpeningBalance,
        Transaction,
    )

    def _entries(lst):
        return [JournalEntry(**e) for e in lst]

    transactions = [
        Transaction(
            transaction_id=t["transaction_id"],
            date=t["date"],
            description=t["description"],
            entries=_entries(t["entries"]),
            tx_type=t.get("tx_type", ""),
            complexity_factors=t.get("complexity_factors", []),
        )
        for t in d["transactions"]
    ]
    adjustments = [
        Adjustment(
            adjustment_id=a["adjustment_id"],
            date=a["date"],
            description=a["description"],
            adj_type=a["adj_type"],
            entries=_entries(a["entries"]),
            calculation=a.get("calculation"),
        )
        for a in d["adjustments"]
    ]
    expected = BalanceSheet(**{k: d["expected"][k] for k in
                               ("date","assets","liabilities","equity",
                                "total_assets","total_liabilities","total_equity","balanced")})
    states = [IntermediateState(**s) for s in d["intermediate_states"]]
    ob = OpeningBalance(**d["opening_balance"])

    return Problem(
        problem_id=d["problem_id"],
        difficulty_level=d["difficulty_level"],
        opening_balance=ob,
        transactions=transactions,
        adjustments=adjustments,
        expected=expected,
        intermediate_states=states,
        metadata=d["metadata"],
    )


def print_summary(model_id: str, strategy: str, results, agg, err_stats):
    print(f"\n{'='*65}")
    print(f"  Model:    {model_id}")
    print(f"  Strategy: {strategy}")
    print(f"  Problems: {agg.n_problems}")
    print(f"{'='*65}")
    print(f"  {'Metric':<30} {'Value':>10}")
    print(f"  {'-'*42}")
    print(f"  {'Balance Accuracy (BA)':<30} {agg.BA*100:>9.1f}%")
    print(f"  {'Account-Level Accuracy (ALA)':<30} {agg.ALA*100:>9.1f}%")
    print(f"  {'Transaction Processing (TPA)':<30} {agg.TPA*100:>9.1f}%")
    print(f"  {'Constraint Satisfaction (CSR)':<30} {agg.CSR*100:>9.1f}%")
    print(f"  {'MAE':<30} {agg.MAE:>10,.0f}")
    print(f"  {'FinBalance Score (FBS)':<30} {agg.FBS:>10.1f}")
    print(f"  {'FBS weighted':<30} {agg.FBS_weighted:>10.1f}")
    print(f"  {'Parse error rate':<30} {agg.parse_error_rate*100:>9.1f}%")

    if agg.by_difficulty:
        print(f"\n  By difficulty:")
        print(f"  {'Level':<8} {'N':>5} {'BA':>7} {'ALA':>7} {'FBS':>7}")
        for lvl in sorted(agg.by_difficulty):
            b = agg.by_difficulty[lvl]
            print(f"  {lvl:<8} {b['n']:>5} {b['BA']*100:>6.1f}% {b['ALA']*100:>6.1f}% {b['FBS']:>7.1f}")

    if err_stats.by_category:
        print(f"\n  Error breakdown:")
        for cat, info in err_stats.by_category.items():
            print(f"  {'  '+cat:<30} {info['count']:>5}  ({info['pct']:.1f}%)")


def main():
    parser = argparse.ArgumentParser(description="Run FinBalance evaluation")
    parser.add_argument("--dataset",    help="Path to JSONL dataset file")
    parser.add_argument("--models",     default="openai/gpt-4.1-mini",
                        help="Comma-separated model IDs")
    parser.add_argument("--strategies", default="zero_shot",
                        help="Comma-separated strategies: zero_shot,few_shot,cot,self_refine")
    parser.add_argument("--output",     default="results",
                        help="Directory to write result JSON files")
    parser.add_argument("--quick",      action="store_true",
                        help="Generate 5 problems per level (L1-L2) instead of loading dataset")
    parser.add_argument("--seed",       type=int, default=42)
    parser.add_argument("--api-key",    default=None,
                        help="API key (overrides ANTHROPIC_API_KEY / OPENROUTER_API_KEY env vars)")
    args = parser.parse_args()

    # Load or generate problems
    if args.quick:
        print("Quick mode: generating 10 problems (5×L1, 5×L2)...")
        gen = ProblemGenerator(seed=args.seed)
        problems = gen.generate_dataset({1: 5, 2: 5})
    elif args.dataset:
        print(f"Loading dataset from {args.dataset}...")
        problems = load_dataset(args.dataset)
    else:
        print("No dataset provided. Use --dataset or --quick")
        sys.exit(1)

    print(f"Loaded {len(problems)} problems.")

    out_dir = Path(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)

    model_ids  = [m.strip() for m in args.models.split(",")]
    strategies = [s.strip() for s in args.strategies.split(",")]

    all_summaries = []

    for model_id in model_ids:
        for strategy in strategies:
            model  = build_model(model_id, api_key=args.api_key, seed=args.seed)
            runner = EvaluationRunner(model, strategy=strategy, verbose=True)

            results = runner.run(problems)

            # Aggregate
            agg = aggregate([r.metrics for r in results])
            err_stats = aggregate_errors(
                [r.errors for r in results],
                [r.difficulty for r in results],
            )

            print_summary(model_id, strategy, results, agg, err_stats)

            # Save
            safe_model = model_id.replace("/", "_")
            out_path = out_dir / f"{safe_model}_{strategy}.json"
            runner.save_results(results, str(out_path))

            all_summaries.append({
                "model":    model_id,
                "strategy": strategy,
                "FBS":      agg.FBS,
                "FBS_w":    agg.FBS_weighted,
                "BA":       agg.BA,
                "ALA":      agg.ALA,
            })

    # Leaderboard
    if len(all_summaries) > 1:
        print("\n\n=== LEADERBOARD (by FBS weighted) ===")
        print(f"{'Model':<35} {'Strategy':<12} {'FBS':>6} {'FBS_w':>7} {'BA':>7} {'ALA':>7}")
        for row in sorted(all_summaries, key=lambda x: -x["FBS_w"]):
            print(
                f"{row['model']:<35} {row['strategy']:<12} "
                f"{row['FBS']:>6.1f} {row['FBS_w']:>7.1f} "
                f"{row['BA']*100:>6.1f}% {row['ALA']*100:>6.1f}%"
            )

        lb_path = out_dir / "leaderboard.json"
        with open(lb_path, "w") as f:
            json.dump(all_summaries, f, indent=2)
        print(f"\nLeaderboard saved to {lb_path}")


if __name__ == "__main__":
    main()
